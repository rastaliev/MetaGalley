          <h2>13. Дополнительный CSS</h2>
          <p>Поле <strong>«Дополнительный CSS»</strong> на рабочем столе (вкладка «Оформление», блок «Дополнительно») добавляет ваши правила <em>в конец</em> автоматически сгенерированных стилей гранки. После правок нажмите «Сохранить оформление гранки». Правила действуют в предпросмотре редактора, в скачанном HTML, в EPUB и при встраивании в OJS (в пределах описанного ниже).</p>

          <h3>13.1. Два варианта разметки: предпросмотр и скачанный HTML</h3>
          <p>Один и тот же набор блоков статьи (заголовок, метаданные, текст и т.д.) вставляется в документ по-разному — важно для селекторов в CSS.</p>

          <h4>Предпросмотр в редакторе (iframe)</h4>
          <ul>
            <li><code>body</code> — страница предпросмотра; на неё навешиваются шрифт, размер, цвет, фон и ограничение ширины из настроек оформления.</li>
            <li>При заданном логотипе: <code>&lt;header class="journal-header"&gt;</code> с <code>&lt;img&gt;</code>; если логотипа нет — у <code>body</code> класс <code>no-logo</code>.</li>
            <li>Весь текст статьи — внутри <code>&lt;div id="content"&gt;</code> (именно к нему удобно привязывать отступы и декоративные линии в превью).</li>
          </ul>

          <h4>Скачанный HTML-файл гранки</h4>
          <ul>
            <li>Корень гранки: <code>&lt;div class="metagalley-granka" id="metagalley-root"&gt;</code> (без логотипа добавляется класс <code>no-logo</code>).</li>
            <li>Логотип: тот же <code>.journal-header</code> с изображением.</li>
            <li>Контент статьи: <code>&lt;main id="article-content"&gt;</code> — внутри него те же <code>h1</code>, <code>.meta-block</code>, <code>.body</code> и остальные блоки, что и в предпросмотре.</li>
            <li>Кнопки «Содержание» и «Наверх» находятся в <code>#float-buttons</code> (стилизовать при необходимости отдельно).</li>
          </ul>
          <div class="info"><strong>EPUB:</strong> в главу попадает в основном HTML контента статьи и общий CSS; обёртки <code>.metagalley-granka</code> и <code>#article-content</code> там нет — ориентируйтесь на те же классы блоков (<code>.body</code>, <code>.meta-block</code> и т.д.) и тег <code>body</code> главы.</div>

