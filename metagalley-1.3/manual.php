          <h3>13.3. Как не конфликтовать с пресетом и ползунками</h3>
          <ul>
            <li>Базовый CSS задаёт фон, цвет текста, шрифт, размер, межстрочный интервал, оформление <code>.meta-block</code>, подчёркивание <code>h2</code>, курсив <code>h3</code>, список литературы и ссылки — всё это приходит из вкладки «Оформление».</li>
            <li>Ваш блок добавляется <strong>после</strong> этих правил. Если селектор и специфичность такие же, ваше объявление перекроет базовое (например, второе правило <code>body { … }</code> в том же файле).</li>
            <li>База использует селекторы с большей специфичностью для части вложенных элементов (например, <code>.meta-block p</code> сильнее, чем одно только <code>p</code>). Чтобы поправить отступы внутри мета-блока, пишите <code>.meta-block p { … }</code>, а не общий <code>p</code>.</li>
            <li>Чтобы <strong>не отменять</strong> настройки журнала, можно добавлять только «надстройки»: рамки, тени, <code>border-left</code>, псевдоэлементы, для цвета линий использовать <code>currentColor</code> с прозрачностью (<code>opacity</code> / <code>rgba</code>) — тогда акцент подстроится под выбранный цвет текста.</li>
          </ul>

          <h3>13.4. Скачанный HTML и <code>!important</code></h3>
          <p>Для встраивания в OJS после вашего дополнительного CSS подключается блок правил с префиксом <code>.metagalley-granka</code>, часть из них с <code>!important</code> (сброс наследования от темы сайта). Из-за этого отдельные ваши правила без <code>!important</code> для <code>h1</code>, <code>p</code>, <code>.meta-block</code> внутри экспорта могут не сработать так же, как в предпросмотре.</p>
          <p><strong>Что делать:</strong> для критичных стилей в скачанной гранке дублируйте селектор с префиксом, например: <code>.metagalley-granka #article-content &gt; h1 { … }</code> или <code>.metagalley-granka .meta-block { … }</code> и при необходимости добавьте <code>!important</code> только к тем свойствам, которые не применяются.</p>

          <h3>13.5. Краткий чек-лист</h3>
